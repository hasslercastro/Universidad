    public void LlenarEquipoCampeon(){
        MySQL db = new MySQL();
        db.MySQLConnect();
        
        DefaultTableModel mod = new DefaultTableModel();
        mod.addColumn("Equipo Campeón");
        TablaCampeon.setModel(mod);
        try{
            db.comando = db.conexion.createStatement();
            db.registro = db.comando.executeQuery("select nombre from Partido_Final,Equipo where Equipo.idEquipo = Partido_Final.idEquipo_Campeon;");
            String [] datos = new String [1];
            while(db.registro.next()){
                datos[0]=db.registro.getString(1);          
                mod.addRow(datos);
                break;
            }
            TablaCampeon.setModel(mod);
            db.close();
        }catch(Exception e){
            System.out.println(e);
        }
        db.close();
    }
