     public void LlenarJugadorGoleador(){
        MySQL db = new MySQL();
        db.MySQLConnect();
        
        DefaultTableModel mod = new DefaultTableModel();
        mod.addColumn("Nombre");
        mod.addColumn("Apellido 1");
        mod.addColumn("Apellido 2");
        mod.addColumn("Goles Anotados");
        TablaJugador.setModel(mod);
        try{
        db.comando = db.conexion.createStatement();
        db.registro = db.comando.executeQuery("select nombre,apellido1,apellido2,num_goles from Jugador group by (idJugador) order by(num_goles) desc limit 1 ;");
        String [] datos = new String [4];
        while(db.registro.next()){
        datos[0]=db.registro.getString(1);
        datos[1]=db.registro.getString(2);
        datos[2]=db.registro.getString(3);    
        datos[3]=db.registro.getString(4);
        mod.addRow(datos);
    
        }
        TablaJugador.setModel(mod);
        db.close();
        }catch(Exception e){
            System.out.println(e);
        }
        db.close();
    }
    
