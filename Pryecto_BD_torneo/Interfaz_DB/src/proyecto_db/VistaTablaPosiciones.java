    private void PedirTablaActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_PedirTablaActionPerformed
        MySQL db = new MySQL();
        db.MySQLConnect();
        
        DefaultTableModel mod = new DefaultTableModel();
        mod.addColumn("Equipo");
        mod.addColumn("Puntos");
        TablaDePosiciones.setModel(mod);
        try{
            
        db.comando = db.conexion.createStatement();
        db.registro = db.comando.executeQuery("select nombre,puntos from Equipo group by idEquipo order by puntos desc;");
        String [] datos = new String [2];
        while(db.registro.next()){
            datos[0]=db.registro.getString(1);
            datos[1]=db.registro.getString(2);
            mod.addRow(datos);
        }

        db.close();
        }catch(Exception e){
            System.out.println(e);
        }    
    }//GEN-LAST:event_PedirTablaActionPerformed

