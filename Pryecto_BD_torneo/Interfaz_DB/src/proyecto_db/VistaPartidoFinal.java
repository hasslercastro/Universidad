    private void AgregarPartidoFinalActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_AgregarPartidoFinalActionPerformed
        Partido partido = new Partido();
        String EquipoL = EquipoLocal.getText();
        String EquipoV = EquipoVisitante.getText();
        String resultado = MarcadorPartidoFinal.getText();
        partido.IngresarPartidoFinal(EquipoL, EquipoV, resultado);
        EquipoLocal.setText("");
        EquipoVisitante.setText("");
        MarcadorPartidoFinal.setText("");
    }//GEN-LAST:event_AgregarPartidoFinalActionPerformed

